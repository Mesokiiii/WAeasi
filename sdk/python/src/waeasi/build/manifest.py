"""waeasi.build.manifest — TOML emitter matching kernel parser."""

from __future__ import annotations

import re
from dataclasses import dataclass, field
from pathlib import Path
from typing import Literal, Optional

from .errors import ConfigError

Right = Literal[
    "FS_READ", "FS_WRITE", "NET_CONNECT", "NET_BIND",
    "CLOCK_WALL", "CLOCK_MONO", "RANDOM_INSEC", "RANDOM_SEC",
]
World = Literal["handler", "scheduled", "stream"]

_NAME_RE = re.compile(r"^[a-z][a-z0-9-]{0,63}$")
_SEMVER_RE = re.compile(r"^\d+\.\d+\.\d+(-[a-zA-Z0-9.-]+)?$")
_BAD_CHARS = re.compile(r'[\\"\n\r\t]')


@dataclass
class ManifestSpec:
    name: str
    version: str
    world: World
    rights: list[Right] = field(default_factory=list)
    cpu_shares: Optional[int] = None
    memory_pages_max: Optional[int] = None
    linear_mem_max: Optional[str] = None
    exports: list[tuple[str, str]] = field(default_factory=list)


@dataclass(frozen=True)
class ManifestInput:
    spec: ManifestSpec
    user_digest: str
    engine_digest: Optional[str]
    sdk_version: str
    out_path: Path


@dataclass(frozen=True)
class ManifestResult:
    manifest_path: Path
    size_bytes: int


def write_manifest(input: ManifestInput) -> ManifestResult:
    _validate(input.spec)
    text = _render(input)
    input.out_path.parent.mkdir(parents=True, exist_ok=True)
    input.out_path.write_text(text, encoding="utf-8")
    return ManifestResult(input.out_path, len(text.encode("utf-8")))


def _render(i: ManifestInput) -> str:
    s = i.spec
    out: list[str] = []
    out.append("# Generated by waeasi-py SDK — do not hand-edit")
    out.append("")
    out.append("[component]")
    out.append(f'name    = {_q(s.name)}')
    out.append(f'version = {_q(s.version)}')
    out.append(f'digest  = {_q("sha256:" + i.user_digest)}')
    if i.engine_digest:
        out.append(f'engine  = {_q("sha256:" + i.engine_digest)}')
    out.append(f'world   = {_q(s.world)}')
    out.append(f'sdk     = {_q("python@" + i.sdk_version)}')
    out.append("")
    out.append("[capabilities]")
    out.append("rights = [" + ", ".join(_q(r) for r in s.rights) + "]")
    out.append("")
    out.append("[resources]")
    if s.cpu_shares is not None:
        out.append(f"cpu_shares       = {s.cpu_shares}")
    if s.memory_pages_max is not None:
        out.append(f"memory_pages_max = {s.memory_pages_max}")
    if s.linear_mem_max is not None:
        out.append(f"linear_mem_max   = {_q(s.linear_mem_max)}")
    out.append("")
    if s.exports:
        out.append("[exports]")
        for k, v in s.exports:
            out.append(f"{k} = {_q(v)}")
        out.append("")
    return "\n".join(out)


def _q(s: str) -> str:
    if _BAD_CHARS.search(s):
        raise ConfigError(f"unsafe char in manifest string: {s!r}")
    return f'"{s}"'


def _validate(s: ManifestSpec) -> None:
    if not _NAME_RE.match(s.name):
        raise ConfigError(f"invalid component name: {s.name}")
    if not _SEMVER_RE.match(s.version):
        raise ConfigError(f"invalid semver: {s.version}")
    if s.cpu_shares is not None and not (1 <= s.cpu_shares <= 10000):
        raise ConfigError(f"cpu_shares out of range [1,10000]: {s.cpu_shares}")
    if s.memory_pages_max is not None and not (1 <= s.memory_pages_max <= 65536):
        raise ConfigError(
            f"memory_pages_max out of range [1,65536]: {s.memory_pages_max}",
        )
    for r in s.rights:
        if r not in (
            "FS_READ", "FS_WRITE", "NET_CONNECT", "NET_BIND",
            "CLOCK_WALL", "CLOCK_MONO", "RANDOM_INSEC", "RANDOM_SEC",
        ):
            raise ConfigError(f"unknown right: {r}")
