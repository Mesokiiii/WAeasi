// @waeasi/sdk — Manifest stage
//
// Writes a `manifest.toml` consumable by `components/manifest/parse.rs`
// (kernel-side parser).  Only the schema fields that parser supports
// are emitted; anything else would be rejected at load time.
//
// Inputs come from the user's `waeasi.config.{ts,js,json}` plus
// derived data (digests, world hash, sdk version).

import { writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";

export type Right =
    | "FS_READ" | "FS_WRITE"
    | "NET_CONNECT" | "NET_BIND"
    | "CLOCK_WALL" | "CLOCK_MONO"
    | "RANDOM_INSEC" | "RANDOM_SEC";

export interface ManifestSpec {
    name: string;
    version: string;
    /** Required: which world the component targets. */
    world: "handler" | "scheduled" | "stream";
    /** Capability rights to request from the kernel. */
    rights: Right[];
    /** Resource limits.  Kernel enforces; values are upper bounds. */
    cpuShares?: number;          // relative weight (1-1000), default 100
    memoryPagesMax?: number;     // 64-KiB Wasm pages
    linearMemMax?: string;       // human-readable, e.g. "16 MiB"
    /** Exports of waeasi:obs/etc. visible from waeasictl inspect. */
    exports?: Array<[string, string]>;
}

export interface ManifestInput {
    spec: ManifestSpec;
    engineDigest: string | null;
    userDigest: string;
    sdkVersion: string;
    outPath: string;
}

export interface ManifestResult {
    manifestPath: string;
    sizeBytes: number;
}

export async function writeManifest(
    input: ManifestInput,
): Promise<ManifestResult> {
    const m = render(input);
    await mkdir(dirname(input.outPath), { recursive: true });
    await writeFile(input.outPath, m, "utf8");
    return {
        manifestPath: resolve(input.outPath),
        sizeBytes: Buffer.byteLength(m, "utf8"),
    };
}

function render(i: ManifestInput): string {
    const s = i.spec;
    validate(s);
    const lines: string[] = [];
    lines.push("# Generated by @waeasi/sdk — do not hand-edit");
    lines.push("");
    lines.push("[component]");
    lines.push(`name    = ${q(s.name)}`);
    lines.push(`version = ${q(s.version)}`);
    lines.push(`digest  = ${q("sha256:" + i.userDigest)}`);
    if (i.engineDigest) {
        lines.push(`engine  = ${q("sha256:" + i.engineDigest)}`);
    }
    lines.push(`world   = ${q(s.world)}`);
    lines.push(`sdk     = ${q("typescript@" + i.sdkVersion)}`);
    lines.push("");

    lines.push("[capabilities]");
    lines.push(`rights = [${s.rights.map(q).join(", ")}]`);
    lines.push("");

    lines.push("[resources]");
    if (s.cpuShares !== undefined)
        lines.push(`cpu_shares       = ${s.cpuShares}`);
    if (s.memoryPagesMax !== undefined)
        lines.push(`memory_pages_max = ${s.memoryPagesMax}`);
    if (s.linearMemMax !== undefined)
        lines.push(`linear_mem_max   = ${q(s.linearMemMax)}`);
    lines.push("");

    if (s.exports && s.exports.length > 0) {
        lines.push("[exports]");
        for (const [k, v] of s.exports) lines.push(`${k} = ${q(v)}`);
        lines.push("");
    }
    return lines.join("\n");
}

function q(s: string): string {
    if (/[\\"\n\r\t]/.test(s)) {
        throw new Error(`unsafe character in manifest string: ${JSON.stringify(s)}`);
    }
    return `"${s}"`;
}

function validate(s: ManifestSpec): void {
    if (!/^[a-z][a-z0-9-]{0,63}$/.test(s.name)) {
        throw new Error(`invalid component name: ${s.name}`);
    }
    if (!/^\d+\.\d+\.\d+(-[a-zA-Z0-9.-]+)?$/.test(s.version)) {
        throw new Error(`invalid semver: ${s.version}`);
    }
    if (s.cpuShares !== undefined
        && (s.cpuShares < 1 || s.cpuShares > 10000)) {
        throw new Error(`cpu_shares out of range [1,10000]: ${s.cpuShares}`);
    }
    if (s.memoryPagesMax !== undefined
        && (s.memoryPagesMax < 1 || s.memoryPagesMax > 65536)) {
        throw new Error(
            `memory_pages_max out of range [1,65536]: ${s.memoryPagesMax}`,
        );
    }
}
