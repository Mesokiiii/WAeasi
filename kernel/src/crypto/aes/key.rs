//! AES key expansion (FIPS 197 § 5.2).
//!
//! AES-128 → 11 round keys (44 32-bit words).
//! AES-256 → 15 round keys (60 32-bit words).
//!
//! Stage-9 software path; AES-NI shortcut lands in stage-10 once the
//! kernel exposes a `cpuid::AES_NI` flag through `arch::x86_64`.
pub type Aes128Key = [u8; 16];
pub type Aes256Key = [u8; 32];

#[derive(Clone)]
pub struct KeySchedule {
    pub round_keys: [[u8; 16]; 15],   // up to 14 + initial = 15
    pub rounds:     u8,                // 10 (AES-128) or 14 (AES-256)
}

